public class Arrays {
    public static void main(String[] args) {
        int[] a = {
             1,3,4,5,6,7
        };
        for (int i =0; i<a.length;i++){
            System.out.println(a[i]);
        }
        for (int i =0; i<5;i++){
            System.out.println(5+a[i]);
        }
    }
}
