public class Arrays {
    public static void main(String[] args) {
        //Array
        int[] a = {
             1,3,4,5,6,7
        };
        for (int j : a) {
            System.out.println(j);
        }
        for (int i =0; i<5;i++){
            System.out.println(5+a[i]);
        }
    }
}
